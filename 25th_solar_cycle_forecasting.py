def mat_model(xi):
    #args1
    A=7.5
    w=11
    off=1976
    off2=6.3
    #args2
    w2=w*8
    A2=0.5
    B2=0.2
    off3=-6
    #overall
    alpha = 1/16
    return alpha*((A+np.cos((xi-off+off2)*2*np.pi/w)*A)**2)*(B2+A2+np.cos((xi-off+off3)*2*np.pi/w2)*A2)
